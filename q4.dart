// Implement a code that finds the factorial of a number using a while
// loop or for loop.
// Example:
// Input: 5
// Output: Factorial of 5 is 120
void main(){
//The factorial of n is denoted by n! and calculated by multiplying the 
//integer numbers from 1 to n. The formula for n factorial is n! = n Ã— (n - 1)!.

  int number = 5;
  int factorial = 1;
  for(int i=0; i<-number; i++){
    factorial*=i
    }
  print("Factorial of $number is $factorial");
}
